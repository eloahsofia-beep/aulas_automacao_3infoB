# IF - Estrutura Condicional (se fulunano faz isso)

#Variável + conversão de srt em real (float) + Entrada
media = float(input("Digite a média do aluno:"))

#Processamento
if media >=6:
    #caminho do sim
    print("Você está aprovado: ")
else:
    #caminho do não
    print("Você foi reprovado: ")